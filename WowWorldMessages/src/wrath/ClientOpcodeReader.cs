    public static async Task<WrathClientMessage> ReadEncryptedAsync(Stream r, WrathClientDecryption decrypter, CancellationToken cancellationToken = default) {
        var header = await decrypter.ReadClientHeaderAsync(r, cancellationToken).ConfigureAwait(false);

        unchecked {
            header.Size -= 4;
        }
        return await ReadBodyAsync(r, header, cancellationToken).ConfigureAwait(false);
    }
    public static async Task<WrathClientMessage> ReadUnencryptedAsync(Stream r, CancellationToken cancellationToken = default) {
        var decrypter = new NullCrypterWrath();
        var header = await decrypter.ReadClientHeaderAsync(r, cancellationToken).ConfigureAwait(false);

        unchecked {
            header.Size -= 4;
        }
        return await ReadBodyAsync(r, header, cancellationToken).ConfigureAwait(false);
    }
